import fs from "node:fs";
import path from "node:path";

import { getRepoRootDir, isMainModule } from "../runtimePaths.js";

// Windows path.join produces backslashes; normalize to forward slashes for
// anything that ends up in a log line or error message (fs calls themselves
// work fine with either separator, so this is purely for display).
function toDisplayPath(value: string): string {
  return value.replaceAll("\\", "/");
}

interface OclifManifestFlag {
  type?: "boolean" | "option";
}

interface OclifManifestCommand {
  flags?: Record<string, OclifManifestFlag>;
}

interface OclifManifest {
  commands: Record<string, OclifManifestCommand>;
}

export interface BrowseCliCommandContract {
  flags: string[];
}

export type BrowseCliContract = Record<string, BrowseCliCommandContract>;

export function defaultManifestPath(): string {
  return path.join(getRepoRootDir(), "packages", "cli", "oclif.manifest.json");
}

export function buildBrowseCliContractFromManifest(
  manifestPath: string,
): BrowseCliContract {
  const raw = fs.readFileSync(manifestPath, "utf8");
  const manifest = JSON.parse(raw) as OclifManifest;

  const contract: BrowseCliContract = {};
  for (const commandId of Object.keys(manifest.commands).sort()) {
    const flags = Object.keys(manifest.commands[commandId].flags ?? {}).sort();
    contract[commandId] = { flags };
  }

  return contract;
}

function renderContractModule(contract: BrowseCliContract): string {
  const entries = Object.entries(contract)
    .map(([commandId, { flags }]) => {
      const flagList = flags.map((flag) => JSON.stringify(flag)).join(", ");
      return `  ${JSON.stringify(commandId)}: { flags: [${flagList}] },`;
    })
    .join("\n");

  return (
    `// AUTO-GENERATED by packages/evals/scripts/generateBrowseCliContract.ts
// Source: packages/cli/oclif.manifest.json (generated by oclif from
// packages/cli/src/commands/**'s own ` +
    "`static flags`" +
    ` declarations at build time).
// Regenerate: pnpm --dir packages/cli build && pnpm --dir packages/evals generate:browse-cli-contract
// Do not hand-edit — a stale entry here silently reintroduces the exact
// command/flag drift this file exists to catch.

export const BROWSE_CLI_CONTRACT = {
${entries}
} as const;

export type BrowseCliCommandId = keyof typeof BROWSE_CLI_CONTRACT;

export function commandAcceptsFlag(
  command: BrowseCliCommandId,
  flag: string,
): boolean {
  return (BROWSE_CLI_CONTRACT[command].flags as readonly string[]).includes(
    flag,
  );
}

const MODE_FLAG_NAME = "local";

export const BROWSE_CLI_MODELESS_COMMANDS: readonly BrowseCliCommandId[] =
  Object.keys(BROWSE_CLI_CONTRACT)
    .filter((commandId) => !commandId.includes(":"))
    .filter(
      (commandId) =>
        !commandAcceptsFlag(
          commandId as BrowseCliCommandId,
          MODE_FLAG_NAME,
        ),
    ) as BrowseCliCommandId[];
`
  );
}

function main(): void {
  const manifestPath = defaultManifestPath();
  if (!fs.existsSync(manifestPath)) {
    throw new Error(
      `browse CLI manifest not found at ${toDisplayPath(manifestPath)}. Run pnpm --dir packages/cli build first.`,
    );
  }

  const contract = buildBrowseCliContractFromManifest(manifestPath);
  const outputPath = path.join(
    getRepoRootDir(),
    "packages",
    "evals",
    "core",
    "tools",
    "browseCliContract.generated.ts",
  );
  fs.writeFileSync(outputPath, renderContractModule(contract));
  console.log(`Wrote ${toDisplayPath(outputPath)}`);
}

if (isMainModule()) {
  main();
}
